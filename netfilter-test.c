char *strnstr(const char *s1, const char *s2, size_t len)
{
    size_t l2;
    l2 = strlen(s2);
    if (!l2)
        return (char *)s1;
    while (len >= l2) {
        len--;
        if (!memcmp(s1, s2, l2))
            return (char *)s1;
        s1++;
    }
    return NULL;
}


int FindHost(char* buf, char* hostname)
{
    char *HostTag="Host";
    char *hostfield = strtok((char*)buf, "\r\n"); //split buf using \r\n


    while (hostfield != NULL)
        {
            if(strnstr(hostfield,HostTag,strlen(hostfield))!=NULL) //Find host field using 'Host'
            {
                if(strnstr(hostfield,hostname,strlen(hostfield))!=NULL) //match the hostname and host field
                {
                    return 0; //match!
                }
                return -1; //find host field but not matched..
             }
            hostfield = strtok(NULL, "\r\n");
        }
    return -1;

}

